CREATE TABLE CLIENTE(
    Nit INT(4) NOT NULL,
    Nombre VARCHAR(40) NOT NULL
);
CREATE TABLE MEDICAMENTO(
    Id_med INT(4) NOT NULL,
    Nombre VARCHAR(40),
    C_comp FLOAT(4),
    C_vent FLOAT(4),
    Venc DATE NOT NULL
);
CREATE TABLE COMPRA(
    Id_med INT(4) NOT NULL,
    Nit INT(4) NOT NULL,
    Id_med INT(4) NOT NULL,
    Saldo  FLOAT(4) NOT NULL,
    Fecha DATE NOT NULL
);
