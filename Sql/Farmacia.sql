    Cant_disp INT(4) NOT NULL
);
CREATE TABLE PROVEDOR(
    Id_pro INT(4) NOT NULL,
    Nombre VARCHAR(40) NOT NULL,
    Telf INT(4) NOT NULL,
    Dir VARCHAR(50) NOT NULL
);
CREATE TABLE PEDIDO(
    Id_ped INT(4) NOT NULL,
    Id_pro INT(4) NOT NULL,
    Id_suc INT(4) NOT NULL,
    Fecha_ped DATE NOT NULL
);
CREATE TABLE LOTE(
    Id_lot INT(4) NOT NULL,
    Id_ped INT(4) NOT NULL,
    Id_med INT(4) NOT NULL,
    Costo FLOAT(4) NOT NULL
);
